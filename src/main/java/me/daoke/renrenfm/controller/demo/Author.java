public class Author {
    private String id;

    private String authorName;

    private String authorRemark;

    private int status;

    private String picUrl;

    private String age;

    public String getDenseNum() {
        return denseNum;
    }

    public void setDenseNum(String denseNum) {
        this.denseNum = denseNum;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAuthorName() {
        return authorName;
    }

    public void setAuthorName(String authorName) {
        this.authorName = authorName;
    }

    public String getAuthorRemark() {
        return authorRemark;
    }

    public void setAuthorRemark(String authorRemark) {
        this.authorRemark = authorRemark;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getPicUrl() {
        return picUrl;
    }

    public void setPicUrl(String picUrl) {
        this.picUrl = picUrl;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    private String denseNum;



}
